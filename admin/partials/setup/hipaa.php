<h1>PHI Information and Policies</h1>
<h2>Understanding Protected Health Information (PHI) and Our App's Policies</h2>

    <h3>What is Protected Health Information (PHI)?</h3>
    <p>
        Protected Health Information (PHI) refers to any information in a medical record that can be used to identify 
        an individual and that was created, used, or disclosed in the course of providing a health care service, such 
        as a diagnosis or treatment. Examples of PHI include names, addresses, birthdates, Social Security numbers, 
        medical records, and even photographs. PHI is regulated under the Health Insurance Portability and Accountability 
        Act (HIPAA) in the United States, which sets strict standards for handling and protecting such information.
    </p>

    <h3>Why is PHI Sensitive?</h3>
    <p>
        PHI is incredibly sensitive because it relates to an individual’s personal health and medical history. Unauthorized 
        access to PHI can lead to privacy violations, identity theft, and other forms of misuse. Therefore, it's crucial 
        to handle this information with the utmost care and confidentiality.
    </p>

    <h3>Our App’s PHI Policy</h3>
    <p>
        In our application, we are committed to safeguarding your privacy and ensuring the security of any personal 
        information. To align with this commitment and comply with legal standards, our app does not allow the storage, 
        processing, or transmission of PHI.
    </p>

    <h3>Why We Do Not Allow PHI</h3>
    <ol>
        <li>
            <strong>Privacy and Security</strong>: Our primary concern is the privacy and security of our users. By not 
            allowing PHI, we minimize the risk of sensitive health information being exposed or misused.
        </li>
        <li>
            <strong>Compliance with Regulations</strong>: Managing PHI requires stringent measures and compliance with 
            healthcare regulations like HIPAA. Our platform is not designed for handling such specific regulatory 
            requirements, and hence, we avoid PHI to ensure legal compliance.
        </li>
        <li>
            <strong>Focus on Core Functionality</strong>: Our app is designed to offer specific functionalities that 
            do not require the handling of PHI. By not involving PHI, we can focus on delivering these services effectively 
            without the complexities of managing highly sensitive health information.
        </li>
    </ol>

    <h3>Managing Your Entries</h3>
    <p>
        We provide tools for you to view and manage your entries. If any entry accidentally contains PHI, you can use 
        our 'Scrub Data' feature to remove this sensitive information.
    </p>

    <h3>Automatic Deletion of Old Entries</h3>
    <p>
        For added security, we also offer an option to automatically delete your old entries. This feature ensures that 
        your data does not remain on our servers indefinitely, further protecting your privacy.
    </p>

    <p>
        We encourage all our users to review their entries and utilize these features to maintain their privacy and comply 
        with our app's policies.
    </p>